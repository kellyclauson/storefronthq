package com.islandsoftware.storefronhq;

public class FabricInventory
{
   private int fatQuarterCount;
   private int quarterYardCount;
   private int halfYardCount;
   private int threeQuarterYardCount;
   private int oneYardCount;

   public int getFatQuarterCount()
   {
      return fatQuarterCount;
   }

   public void setFatQuarterCount(int fatQuarterCount)
   {
      this.fatQuarterCount = fatQuarterCount;
   }

   public int getQuarterYardCount()
   {
      return quarterYardCount;
   }

   public void setQuarterYardCount(int quarterYardCount)
   {
      this.quarterYardCount = quarterYardCount;
   }

   public int getHalfYardCount()
   {
      return halfYardCount;
   }

   public void setHalfYardCount(int halfYardCount)
   {
      this.halfYardCount = halfYardCount;
   }

   public int getThreeQuarterYardCount()
   {
      return threeQuarterYardCount;
   }

   public void setThreeQuarterYardCount(int threeQuarterYardCount)
   {
      this.threeQuarterYardCount = threeQuarterYardCount;
   }

   public int getOneYardCount()
   {
      return oneYardCount;
   }

   public void setOneYardCount(int oneYardCount)
   {
      this.oneYardCount = oneYardCount;
   }
}
